import { writeFileSync } from "node:fs";
import { createRequire } from "node:module";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const currentDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(currentDir, "..");
const require = createRequire(resolve(repoRoot, "apps/desktop/package.json"));
const { Scanner } = require("@tailwindcss/oxide");
const outputPath = resolve(
  repoRoot,
  "apps/desktop/src/renderer/src/styles/streamdown-prefix-source.txt"
);
const prefix = "kmuxsd";
const scanner = new Scanner({
  sources: [
    {
      base: repoRoot,
      pattern: "node_modules/streamdown/dist/*.js",
      negated: false
    },
    {
      base: repoRoot,
      pattern: "node_modules/@streamdown/code/dist/*.js",
      negated: false
    },
    {
      base: repoRoot,
      pattern: "node_modules/@streamdown/mermaid/dist/*.js",
      negated: false
    },
    {
      base: repoRoot,
      pattern: "node_modules/@streamdown/math/dist/*.js",
      negated: false
    },
    {
      base: repoRoot,
      pattern: "node_modules/@streamdown/cjk/dist/*.js",
      negated: false
    }
  ]
});
const candidates = scanner
  .scan()
  .map((candidate) => `${prefix}:${candidate}`)
  .sort();
const output = [
  "# Generated by scripts/generate-streamdown-tailwind-source.mjs.",
  "# This gives Tailwind statically detectable candidates for Streamdown's runtime prefix prop.",
  ...candidates,
  ""
].join("\n");

writeFileSync(outputPath, output);
